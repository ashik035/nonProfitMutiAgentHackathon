function generateEventsSql(): string {
  const eventIds = DEMO_MANAGED_EVENTS.map((e) => seedUuid(e.id));
  const ticketIds: string[] = [];
  const speakerIds: string[] = [];
  const agendaIds: string[] = [];
  const registrantIds: string[] = [];

  for (const e of DEMO_MANAGED_EVENTS) {
    e.ticketTypes.forEach((t, i) => ticketIds.push(seedUuid(`${e.id}:ticket:${i}:${t.tier}`)));
    e.speakers.forEach((s, i) => speakerIds.push(seedUuid(`${e.id}:speaker:${i}:${s.name}`)));
    e.agenda.forEach((a, i) => agendaIds.push(seedUuid(`${e.id}:agenda:${i}:${a.time}`)));
    e.registrants.forEach((r) => registrantIds.push(seedUuid(r.id)));
  }

  const lines: string[] = [
    fileHeader("Nonprofit Events (lifecycle)"),
    `DELETE FROM public.nonprofit_event_registrants WHERE id IN (${registrantIds.map((id) => `'${id}'`).join(", ")});`,
    `DELETE FROM public.nonprofit_event_agenda_items WHERE id IN (${agendaIds.map((id) => `'${id}'`).join(", ")});`,
    `DELETE FROM public.nonprofit_event_speakers WHERE id IN (${speakerIds.map((id) => `'${id}'`).join(", ")});`,
    `DELETE FROM public.nonprofit_event_ticket_types WHERE id IN (${ticketIds.map((id) => `'${id}'`).join(", ")});`,
    `DELETE FROM public.nonprofit_events WHERE id IN (${eventIds.map((id) => `'${id}'`).join(", ")});`,
    "",
    "INSERT INTO public.nonprofit_events (",
    "  id, created_by, title, status, date, location, description, capacity, fund_raised",
    ") VALUES",
  ];

  const eventRows = DEMO_MANAGED_EVENTS.map((e, i) => {
    const parts = [
      `'${seedUuid(e.id)}'`,
      "(SELECT id FROM auth.users ORDER BY created_at LIMIT 1)",
      sqlStr(e.title),
      sqlStr(e.status),
      sqlStr(parseDemoDate(e.date, e.id)),
      sqlStr(e.location),
      sqlStr(e.description),
      sqlNum(e.capacity),
      e.fundRaised != null ? sqlNum(e.fundRaised) : "NULL",
    ];
    const suffix = i < DEMO_MANAGED_EVENTS.length - 1 ? "," : "";
    return `  (${parts.join(", ")})${suffix}`;
  });

  lines.push(...eventRows);
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  title = EXCLUDED.title,",
    "  status = EXCLUDED.status,",
    "  date = EXCLUDED.date,",
    "  location = EXCLUDED.location,",
    "  description = EXCLUDED.description,",
    "  capacity = EXCLUDED.capacity,",
    "  fund_raised = EXCLUDED.fund_raised,",
    "  updated_at = now();",
    ""
  );

  // Ticket types
  lines.push(
    "INSERT INTO public.nonprofit_event_ticket_types (",
    "  id, event_id, tier, price, capacity, sold",
    ") VALUES"
  );
  const ticketRows: string[] = [];
  for (const e of DEMO_MANAGED_EVENTS) {
    e.ticketTypes.forEach((t, i) => {
      ticketRows.push(
        `  ('${seedUuid(`${e.id}:ticket:${i}:${t.tier}`)}', '${seedUuid(e.id)}', ${sqlStr(t.tier)}, ${sqlNum(t.price)}, ${sqlNum(t.capacity)}, ${sqlNum(t.sold)})`
      );
    });
  }
  lines.push(ticketRows.join(",\n"));
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  tier = EXCLUDED.tier,",
    "  price = EXCLUDED.price,",
    "  capacity = EXCLUDED.capacity,",
    "  sold = EXCLUDED.sold;",
    ""
  );

  // Speakers
  const speakerRows: string[] = [];
  for (const e of DEMO_MANAGED_EVENTS) {
    e.speakers.forEach((s, i) => {
      speakerRows.push(
        `  ('${seedUuid(`${e.id}:speaker:${i}:${s.name}`)}', '${seedUuid(e.id)}', ${sqlStr(s.name)}, ${sqlStr(s.title)}, ${sqlStr(s.organization)}, ${i})`
      );
    });
  }
  if (speakerRows.length > 0) {
    lines.push(
      "INSERT INTO public.nonprofit_event_speakers (",
      "  id, event_id, name, title, bio, display_order",
      ") VALUES"
    );
    lines.push(speakerRows.join(",\n"));
    lines.push(
      "ON CONFLICT (id) DO UPDATE SET",
      "  name = EXCLUDED.name,",
      "  title = EXCLUDED.title,",
      "  bio = EXCLUDED.bio,",
      "  display_order = EXCLUDED.display_order;",
      ""
    );
  }

  // Agenda
  lines.push(
    "INSERT INTO public.nonprofit_event_agenda_items (",
    "  id, event_id, time, title, speaker_name, display_order",
    ") VALUES"
  );
  const agendaRows: string[] = [];
  for (const e of DEMO_MANAGED_EVENTS) {
    e.agenda.forEach((a, i) => {
      agendaRows.push(
        `  ('${seedUuid(`${e.id}:agenda:${i}:${a.time}`)}', '${seedUuid(e.id)}', ${sqlStr(a.time)}, ${sqlStr(a.title)}, ${sqlStr(a.speaker ?? null)}, ${i})`
      );
    });
  }
  lines.push(agendaRows.join(",\n"));
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  time = EXCLUDED.time,",
    "  title = EXCLUDED.title,",
    "  speaker_name = EXCLUDED.speaker_name,",
    "  display_order = EXCLUDED.display_order;",
    ""
  );

  // Registrants
  lines.push(
    "INSERT INTO public.nonprofit_event_registrants (",
    "  id, event_id, name, email, ticket_tier, checked_in, registered_at",
    ") VALUES"
  );
  const registrantRows: string[] = [];
  for (const e of DEMO_MANAGED_EVENTS) {
    for (const r of e.registrants) {
      registrantRows.push(
        `  ('${seedUuid(r.id)}', '${seedUuid(e.id)}', ${sqlStr(r.name)}, ${sqlStr(r.email)}, ${sqlStr(r.ticketTier)}, ${sqlBool(r.checkedIn)}, '${parseDemoTimestamp(r.registeredDate, r.id)}')`
      );
    }
  }
  lines.push(registrantRows.join(",\n"));
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  name = EXCLUDED.name,",
    "  email = EXCLUDED.email,",
    "  ticket_tier = EXCLUDED.ticket_tier,",
    "  checked_in = EXCLUDED.checked_in,",
    "  registered_at = EXCLUDED.registered_at;",
    ""
  );

  return lines.join("\n");
}

