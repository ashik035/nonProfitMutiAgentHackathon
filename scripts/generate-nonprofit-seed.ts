/**
 * Generates idempotent nonprofit seed SQL from nonprofitDemoData.ts.
 * Run: npm run seed:nonprofit:generate
 */

import { createHash } from "node:crypto";
import { writeFileSync } from "node:fs";
import { join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { parse, format } from "date-fns";
import {
  DEMO_MEMBERS,
  DEMO_VOLUNTEERS,
  DEMO_CAMPAIGNS,
  DEMO_DONATIONS_RECENT,
  DEMO_MANAGED_EVENTS,
  DEMO_PROGRAMS,
} from "../src/shared/data/nonprofitDemoData.ts";

const SEED_DIR = join(import.meta.dirname, "../supabase/seed");
const DATE_FMT = "MMM d, yyyy";

const AUTH_GUARD = `-- Abort if no auth users (created_by subquery would be NULL)
DO $$
BEGIN
  IF (SELECT COUNT(*) FROM auth.users) = 0 THEN
    RAISE EXCEPTION 'SEED ABORTED: No users in auth.users. Sign up via the app first.';
  END IF;
END $$;

`;

/** Deterministic UUID from demo id (stable across re-runs). */
export function seedUuid(demoId: string): string {
  const hash = createHash("sha256").update(`nonprofitctdemo-seed:${demoId}`).digest("hex");
  return [
    hash.slice(0, 8),
    hash.slice(8, 12),
    `4${hash.slice(13, 16)}`,
    `8${hash.slice(17, 20)}`,
    hash.slice(20, 32),
  ].join("-");
}

function sqlStr(value: string | null | undefined): string {
  if (value == null) return "NULL";
  return `'${value.replace(/'/g, "''")}'`;
}

function sqlBool(value: boolean): string {
  return value ? "TRUE" : "FALSE";
}

function sqlNum(value: number | null | undefined): string {
  if (value == null) return "NULL";
  return String(value);
}

function sqlTextArray(values: string[]): string {
  if (values.length === 0) return "ARRAY[]::text[]";
  return `ARRAY[${values.map((v) => sqlStr(v)).join(", ")}]`;
}

function parseDemoDate(raw: string, context: string): string {
  const parsed = parse(raw, DATE_FMT, new Date());
  if (Number.isNaN(parsed.getTime())) {
    throw new Error(`Failed to parse date "${raw}" for ${context} (expected ${DATE_FMT})`);
  }
  return format(parsed, "yyyy-MM-dd");
}

function parseDemoTimestamp(raw: string, context: string): string {
  const date = parseDemoDate(raw, context);
  return `${date} 12:00:00+00`;
}

function fileHeader(name: string): string {
  return `-- ============================================================
-- SEED: ${name}
-- AUTO-GENERATED by scripts/generate-nonprofit-seed.ts
-- Source: src/shared/data/nonprofitDemoData.ts
-- Safe to re-run (idempotent upserts on fixed seed UUIDs).
-- Prerequisite: at least one row in auth.users
-- ============================================================

${AUTH_GUARD}`;
}

function generateMembersSql(): string {
  const ids = DEMO_MEMBERS.map((m) => seedUuid(m.id));
  const lines: string[] = [
    fileHeader("Nonprofit Members"),
    `DELETE FROM public.nonprofit_members WHERE id IN (${ids.map((id) => `'${id}'`).join(", ")});`,
    "",
    "INSERT INTO public.nonprofit_members (",
    "  id, created_by, name, email, phone, tier, status, join_date, renewal_date, employer, interests",
    ") VALUES",
  ];

  const rows = DEMO_MEMBERS.map((m, i) => {
    const parts = [
      `'${seedUuid(m.id)}'`,
      "(SELECT id FROM auth.users ORDER BY created_at LIMIT 1)",
      sqlStr(m.name),
      sqlStr(m.email),
      sqlStr(m.phone ?? null),
      sqlStr(m.tier),
      sqlStr(m.status),
      sqlStr(parseDemoDate(m.joinDate, m.id)),
      sqlStr(parseDemoDate(m.renewalDate, m.id)),
      sqlStr(m.employer ?? null),
      sqlTextArray(m.interests),
    ];
    const suffix = i < DEMO_MEMBERS.length - 1 ? "," : "";
    return `  (${parts.join(", ")})${suffix}`;
  });

  lines.push(...rows);
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  name = EXCLUDED.name,",
    "  email = EXCLUDED.email,",
    "  phone = EXCLUDED.phone,",
    "  tier = EXCLUDED.tier,",
    "  status = EXCLUDED.status,",
    "  join_date = EXCLUDED.join_date,",
    "  renewal_date = EXCLUDED.renewal_date,",
    "  employer = EXCLUDED.employer,",
    "  interests = EXCLUDED.interests,",
    "  updated_at = now();",
    ""
  );
  return lines.join("\n");
}

function generateVolunteersSql(): string {
  const volunteerIds = DEMO_VOLUNTEERS.map((v) => seedUuid(v.id));
  const shiftIds = DEMO_VOLUNTEERS.flatMap((v) => v.shifts.map((s) => seedUuid(s.id)));

  const lines: string[] = [
    fileHeader("Nonprofit Volunteers + Shifts"),
    `DELETE FROM public.nonprofit_volunteer_shifts WHERE id IN (${shiftIds.map((id) => `'${id}'`).join(", ")});`,
    `DELETE FROM public.nonprofit_volunteers WHERE id IN (${volunteerIds.map((id) => `'${id}'`).join(", ")});`,
    "",
    "INSERT INTO public.nonprofit_volunteers (",
    "  id, created_by, name, email, phone, skills, availability, total_hours, joined_date, is_also_donor, donor_total_giving",
    ") VALUES",
  ];

  const volunteerRows = DEMO_VOLUNTEERS.map((v, i) => {
    const parts = [
      `'${seedUuid(v.id)}'`,
      "(SELECT id FROM auth.users ORDER BY created_at LIMIT 1)",
      sqlStr(v.name),
      sqlStr(v.email),
      "NULL",
      sqlTextArray(v.skills),
      sqlTextArray(v.availability),
      sqlNum(v.totalHours),
      sqlStr(parseDemoDate(v.joinedDate, v.id)),
      sqlBool(v.isAlsoDonor),
      sqlNum(v.donorTotalGiving ?? null),
    ];
    const suffix = i < DEMO_VOLUNTEERS.length - 1 ? "," : "";
    return `  (${parts.join(", ")})${suffix}`;
  });

  lines.push(...volunteerRows);
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  name = EXCLUDED.name,",
    "  email = EXCLUDED.email,",
    "  skills = EXCLUDED.skills,",
    "  availability = EXCLUDED.availability,",
    "  total_hours = EXCLUDED.total_hours,",
    "  joined_date = EXCLUDED.joined_date,",
    "  is_also_donor = EXCLUDED.is_also_donor,",
    "  donor_total_giving = EXCLUDED.donor_total_giving,",
    "  updated_at = now();",
    ""
  );

  lines.push(
    "INSERT INTO public.nonprofit_volunteer_shifts (",
    "  id, volunteer_id, event_name, date, hours, status",
    ") VALUES"
  );

  const shiftRows: string[] = [];
  for (const v of DEMO_VOLUNTEERS) {
    for (const s of v.shifts) {
      shiftRows.push(
        `  ('${seedUuid(s.id)}', '${seedUuid(v.id)}', ${sqlStr(s.eventName)}, ${sqlStr(parseDemoDate(s.date, s.id))}, ${sqlNum(s.hours)}, ${sqlStr(s.status)})`
      );
    }
  }
  lines.push(shiftRows.join(",\n"));
  lines.push(
    "ON CONFLICT (id) DO UPDATE SET",
    "  volunteer_id = EXCLUDED.volunteer_id,",
    "  event_name = EXCLUDED.event_name,",
    "  date = EXCLUDED.date,",
    "  hours = EXCLUDED.hours,",
    "  status = EXCLUDED.status,",
    "  updated_at = now();",
    ""
  );

  return lines.join("\n");
}

